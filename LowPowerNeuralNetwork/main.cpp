void evaluateDNN() {
    FILE *fpTestImages;
    FILE *fpTestLabels;

    fpTestImages = fopen(TEST_PATH, "r");
    if (fpTestImages == NULL) {
        fprintf(stderr, "Unable to open %s.", TEST_PATH);
        return;
    }

    fpTestLabels = fopen(TEST_LABELS_PATH, "r");
    if (fpTestLabels == NULL) {
        fprintf(stderr, "Unable to open %s.", TEST_LABELS_PATH);
        return;
    }

    char visualizeSamples[10];
    char sampleToVisualize[10];
    printf("Evaluating the deep neural network..\n");
    printf("Do you want to visualize the samples of a letter? [Y/N]: ");
    scanf("%c", visualizeSamples);
    if (visualizeSamples[0] == 'Y') {
        printf("Insert the letter that you want to visualize the samples of..: ");
        scanf("%s", sampleToVisualize);
    }

    int toPredict[INPUT_IMAGE_SIZE][INPUT_IMAGE_SIZE];
    int i = 0;
    int j = 0;
    float nextVal;
    float labelFloat;
    int label;
    int predictCnt = 0;
    int correctCnt = 0;
    Prediction letters[36];
    for (int i=0; i<36; i++)
        letters[i].letter = fromLabelToCharacter(i);

    while (fscanf(fpTestImages, "%f%*c", &nextVal) > 0) {
        toPredict[i][j] = nextVal;
        j++;

        if (j >= 28) {
            i++;
            j = 0;
            if (i >= 28) {
                fscanf(fpTestLabels, "%f", &labelFloat);
                label = labelFloat;
                predictCnt++;
                letters[label].predictionCnt++;

                int predicted = dnn.inference(toPredict);

                //printf("Accuracy: %f\n", (float)correctCnt/(float)predictCnt);
                fflush(stdout);

                if (visualizeSamples[0] == 'Y' && fromLabelToCharacter(label) == sampleToVisualize[0]) {
                    printImage(toPredict);
                    printf("Label to predict: %d (%c)\n", label, fromLabelToCharacter(label));
                    printf("Label predicted: %d (%c)\n\n", predicted, fromLabelToCharacter(predicted));
                }

                if (predicted == label) {
                    correctCnt++;
                    letters[label].correctPredCnt++;
                }
                i = 0;
            }
        }
    }
    printf("Total predicted: %d\n", predictCnt);
    printf("Correct predictions: %d\n", correctCnt);
    printf("Accuracy: %f\n", (float)correctCnt/(float)predictCnt);

    printf("Per letter stats:\n");
    for (int i=0; i<36; i++) {
        printf("Letter: %c\n", letters[i].letter);
        printf("Total predicted: %d\n", letters[i].predictionCnt);
        printf("Correct predictions: %d\n", letters[i].correctPredCnt);
        printf("Accuracy: %f\n\n", (float)letters[i].correctPredCnt/(float)letters[i].predictionCnt);
    }

    fclose(fpTestImages);
    fclose(fpTestLabels);
}

